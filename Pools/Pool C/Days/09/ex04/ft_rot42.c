char	*ft_rot42(char *str)
{
	int		index;
	char	c;

	if (str == 0)
		return (0);
	index = 0;
	c = str[index];
	while (str[index])
	{
		if (c >= 'A' && c <= 'Z')
		{
			c += 42;
			while (!(c >= 'A' && c <= 'Z'))
				c -= 26;
			str[index] = c;
		}
		if (c >= 'a' && c <= 'z')
		{
			c += 42;
			while (!(c >= 'a' && c <= 'z'))
				c -= 26;
			str[index] = c;
		}
	}
	return (str);
}
