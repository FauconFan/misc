#include "ft_malloc.h"

t_bool	ft_strequ(char *s1, char *s2)
{
	while (*s1 == *s2)
	{
		if (*s1 == 0)
			break ;
		s1++;
		s2++;
	}
	return (*s1 == *s2);
}
