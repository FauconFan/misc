#include "ft_nm_otool.h"

t_bool	ft_isprint(char c)
{
	if (c >= 32 && c < 127)
		return (TRUE);
	if (c == ' ' || c == '\n' || c == '\t' || c == '\v' || c == '\r')
		return (TRUE);
	return (FALSE);
}
