#include "ft_nm_otool.h"

t_bool		ft_strequ(const char *s1, const char *s2)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == 0)
			break ;
		++i;
	}
	return (s1[i] == s2[i]);
}
